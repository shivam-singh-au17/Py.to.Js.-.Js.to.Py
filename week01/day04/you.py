# PERCENTAGE CALCULATOR
# Given the 3 subjects marks of a particular student out of 100 calculate the percentage scored by the student. Let the marks be 73, 85, 62


stu1 = 73
stu2 = 85
stu3 = 62

totatl_marks = 100 * 3

presnt_stu = ((stu1 + stu2 + stu3) / totatl_marks) * 100
print(presnt_stu)



